- What are the qualifications of the providers who will treat me?
- How do you handle medical emergencies or adverse reactions?
- What is included in the consultation fee?
- Can I see the same provider for continuity of care?
- How do you coordinate with my primary care provider?
- What is your cancellation and refund policy?
- How is my medical information protected?

