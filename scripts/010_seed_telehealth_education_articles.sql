## Verify Provider Credentials

The most fundamental step in evaluating any telehealth service is verifying that their healthcare providers are properly licensed and qualified. Key credentials to verify include:

- **Medical licensure:** Providers must be licensed in the state where you reside. You can verify this through your state medical board
- **Board certification:** Look for providers who are board-certified in relevant specialties such as internal medicine, endocrinology, or obesity medicine
- **Prescribing authority:** Only certain licensed professionals can prescribe medications. Verify that prescribing providers have appropriate credentials
- **Continuing education:** Quality providers stay current with evolving medical evidence and guidelines

