## Evaluate the Assessment Process

A comprehensive initial assessment is a hallmark of quality care. Be cautious of services that:

- Prescribe medications based solely on a brief online questionnaire
- Do not require a real-time video consultation with a licensed provider
- Skip detailed medical history review
- Do not ask about current medications and potential interactions
- Fail to assess contraindications before prescribing

A thorough evaluation should include detailed medical history review, assessment of current health conditions and medications, discussion of goals and expectations, explanation of treatment options, and clear informed consent processes.

## Red Flags to Watch For

Several warning signs suggest a telehealth service may not provide adequate care:

- **Guaranteed results:** No legitimate provider can guarantee specific outcomes
- **No medical evaluation:** Medications prescribed without proper evaluation are potentially dangerous
- **Pressure to purchase:** High-pressure sales tactics suggest the priority is revenue, not your health
- **Bundled products:** Required purchases of supplements or proprietary products alongside medical care
- **Difficult cancellation:** Legitimate services make it straightforward to discontinue
- **Lack of transparency:** Unclear pricing, hidden fees, or vague information about providers
- **No emergency protocols:** Quality services have clear processes for handling adverse events

## Insurance and Cost Considerations

Understanding the financial aspects of telehealth care is important:

- Ask whether the service accepts your insurance or provides documentation for reimbursement
- Request a complete breakdown of all costs before enrolling
- Understand what happens to ongoing prescriptions if you discontinue the service
- Compare costs across multiple services, including what is and is not included
- Be wary of services requiring long-term financial commitments upfront

