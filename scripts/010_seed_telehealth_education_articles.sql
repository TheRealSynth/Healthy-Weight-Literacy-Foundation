- **Both approaches** offer legitimate pathways to quality weight management care
- **In-person care** excels in physical examination and complex assessment situations
- **Telehealth** provides advantages in accessibility, convenience, and consistency of follow-up
- **A hybrid approach** may provide the best of both worlds for many people
- **Personal factors** including location, schedule, health complexity, and preferences should guide your choice

## When to Seek Professional Care

Discuss your care delivery preferences with your healthcare provider. They can help you determine which approach is most appropriate for your specific health needs.

**Seek emergency care immediately if** you experience acute medical symptoms. Neither telehealth nor routine in-person care is appropriate for medical emergencies.

